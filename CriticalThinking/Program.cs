using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CriticalThinking
{
    class Program
    {
        static void Main(string[] args)
        {
            int counter = 5;

            // while loop will run 20 times.
            // Same as a for loop just written differently.
            while (counter < 25)
            {
                counter++;
            }
        }
    }
